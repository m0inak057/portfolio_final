from django.db import models
from django.utils import timezone


class Certificate(models.Model):
    """
    Represents a certificate earned by the portfolio owner.
    Can be uploaded as PDF and managed through the admin panel.
    """
    
    name = models.CharField(
        max_length=255,
        help_text="Certificate name to display on portfolio (e.g., 'AWS Certified Solutions Architect')"
    )
    issuer = models.CharField(
        max_length=255,
        help_text="Organization that issued the certificate (e.g., 'Amazon Web Services')"
    )
    issued_date = models.DateField(
        help_text="Date when the certificate was issued"
    )
    pdf_file = models.FileField(
        upload_to='certificates/pdfs/',
        help_text="PDF file of the certificate"
    )
    is_visible = models.BooleanField(
        default=True,
        help_text="Display this certificate on the portfolio"
    )
    created_at = models.DateTimeField(
        auto_now_add=True,
        help_text="When this certificate record was created"
    )
    updated_at = models.DateTimeField(
        auto_now=True,
        help_text="Last time this certificate was updated"
    )
    
    class Meta:
        ordering = ['-issued_date']
        verbose_name = "Certificate"
        verbose_name_plural = "Certificates"
        indexes = [
            models.Index(fields=['is_visible', '-issued_date']),
        ]
    
    def __str__(self):
        return f"{self.name} - {self.issuer} ({self.issued_date.year})"


class Project(models.Model):
    """
    Represents a GitHub repository that is displayed in the portfolio.
    Synced from GitHub API and enhanced with AI-generated content.
    """
    
    CATEGORY_CHOICES = [
        ('MAJOR', 'Major Project'),
        ('OTHER', 'Other / Lab'),
    ]
    
    # GitHub Mapping
    github_id = models.IntegerField(unique=True, help_text="GitHub repository ID")
    repo_name = models.CharField(max_length=255, help_text="Repository name")
    repo_url = models.URLField(help_text="Full GitHub repository URL")
    repo_description = models.TextField(blank=True, null=True, help_text="Original GitHub repository description")
    
    # Admin Controls
    is_visible = models.BooleanField(
        default=False,
        help_text="Display this project in the public portfolio"
    )
    category = models.CharField(
        max_length=10,
        choices=CATEGORY_CHOICES,
        default='OTHER',
        help_text="Project type: MAJOR shows full details, OTHER shows compact view"
    )
    
    # AI-Generated Content
    ai_title = models.CharField(
        max_length=255,
        blank=True,
        help_text="AI-generated project title"
    )
    ai_summary = models.TextField(
        blank=True,
        help_text="AI-generated project summary (paragraphs for MAJOR, 1-liner for OTHER)"
    )
    key_features = models.JSONField(
        default=list,
        blank=True,
        help_text="List of key features generated by AI"
    )
    tech_stack = models.JSONField(
        default=list,
        blank=True,
        help_text="List of technologies used (extracted by AI)"
    )
    
    # Metadata
    last_synced = models.DateTimeField(
        auto_now=True,
        help_text="Last time this project was synced with GitHub"
    )
    ai_generated_at = models.DateTimeField(
        null=True,
        blank=True,
        help_text="Timestamp when AI content was generated"
    )
    created_at = models.DateTimeField(
        auto_now_add=True,
        help_text="When this project record was created"
    )
    
    class Meta:
        ordering = ['-last_synced']
        verbose_name = "Project"
        verbose_name_plural = "Projects"
        indexes = [
            models.Index(fields=['is_visible', '-last_synced']),
            models.Index(fields=['category', 'is_visible']),
        ]
    
    def __str__(self):
        return f"{self.repo_name} ({self.get_category_display()})"
    
    def has_ai_content(self):
        """Check if AI content has been generated"""
        return bool(self.ai_title and self.ai_summary)
    
    def mark_ai_generated(self):
        """Update the timestamp when AI content is generated"""
        self.ai_generated_at = timezone.now()


class Comment(models.Model):
    """
    Represents a comment on a project made by a visitor.
    Requires admin moderation before being visible.
    """
    
    STATUS_CHOICES = [
        ('PENDING', 'Pending Approval'),
        ('APPROVED', 'Approved'),
        ('REJECTED', 'Rejected'),
    ]
    
    project = models.ForeignKey(
        Project,
        on_delete=models.CASCADE,
        related_name='comments',
        help_text="The project this comment is for"
    )
    author_name = models.CharField(
        max_length=255,
        help_text="Name of the person commenting"
    )
    author_email = models.EmailField(
        help_text="Email of the person commenting"
    )
    content = models.TextField(
        help_text="Comment content"
    )
    status = models.CharField(
        max_length=10,
        choices=STATUS_CHOICES,
        default='PENDING',
        help_text="Approval status of the comment"
    )
    created_at = models.DateTimeField(
        auto_now_add=True,
        help_text="When the comment was created"
    )
    approved_at = models.DateTimeField(
        null=True,
        blank=True,
        help_text="When the comment was approved"
    )
    
    class Meta:
        ordering = ['-created_at']
        verbose_name = "Comment"
        verbose_name_plural = "Comments"
        indexes = [
            models.Index(fields=['project', 'status']),
            models.Index(fields=['-created_at']),
        ]
    
    def __str__(self):
        return f"Comment by {self.author_name} on {self.project.repo_name}"


class ContactMessage(models.Model):
    """
    Represents a contact form submission from a visitor.
    Admin can view and respond to messages.
    """
    
    STATUS_CHOICES = [
        ('NEW', 'New'),
        ('READ', 'Read'),
        ('REPLIED', 'Replied'),
        ('ARCHIVED', 'Archived'),
    ]
    
    name = models.CharField(
        max_length=255,
        help_text="Visitor's name"
    )
    email = models.EmailField(
        help_text="Visitor's email address"
    )
    subject = models.CharField(
        max_length=255,
        help_text="Message subject"
    )
    message = models.TextField(
        help_text="Message content"
    )
    status = models.CharField(
        max_length=10,
        choices=STATUS_CHOICES,
        default='NEW',
        help_text="Status of the contact message"
    )
    created_at = models.DateTimeField(
        auto_now_add=True,
        help_text="When the message was sent"
    )
    read_at = models.DateTimeField(
        null=True,
        blank=True,
        help_text="When the message was read"
    )
    
    class Meta:
        ordering = ['-created_at']
        verbose_name = "Contact Message"
        verbose_name_plural = "Contact Messages"
        indexes = [
            models.Index(fields=['status', '-created_at']),
        ]
    
    def __str__(self):
        return f"Contact from {self.name} - {self.subject}"


