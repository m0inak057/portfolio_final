class AboutProfile(models.Model):
    """
    Singleton model for portfolio owner's about information.
    Only one record should exist in the database.
    """
    
    professional_summary = models.TextField(
        help_text="Your professional summary/bio (appears on About section)"
    )
    resume_file = models.FileField(
        upload_to='resume/',
        help_text="Your resume/CV PDF to download"
    )
    github_url = models.URLField(
        blank=True,
        help_text="GitHub profile URL"
    )
    linkedin_url = models.URLField(
        blank=True,
        help_text="LinkedIn profile URL"
    )
    twitter_url = models.URLField(
        blank=True,
        help_text="Twitter/X profile URL"
    )
    other_url = models.URLField(
        blank=True,
        help_text="Any other social/portfolio URL"
    )
    updated_at = models.DateTimeField(
        auto_now=True,
        help_text="Last updated timestamp"
    )
    
    class Meta:
        verbose_name = "About Profile"
        verbose_name_plural = "About Profile"
    
    def __str__(self):
        return "Portfolio Owner Profile"
    
    def save(self, *args, **kwargs):
        """Ensure only one record exists"""
        if AboutProfile.objects.exists() and not self.pk:
            obj = AboutProfile.objects.first()
            obj.professional_summary = self.professional_summary
            obj.resume_file = self.resume_file
            obj.github_url = self.github_url
            obj.linkedin_url = self.linkedin_url
            obj.twitter_url = self.twitter_url
            obj.other_url = self.other_url
            obj.save()
            return
        super().save(*args, **kwargs)


class Skill(models.Model):
    """
    Represents a technical skill or technology.
    Can be manually added or auto-generated from project tech stacks.
    """
    
    CATEGORY_CHOICES = [
        ('LANGUAGE', 'Programming Language'),
        ('FRAMEWORK', 'Framework / Library'),
        ('TOOL', 'Tool / Platform'),
        ('DATABASE', 'Database / Storage'),
        ('OTHER', 'Other'),
    ]
    
    name = models.CharField(
        max_length=100,
        unique=True,
        help_text="Skill name (e.g., 'Python', 'Django', 'React')"
    )
    category = models.CharField(
        max_length=20,
        choices=CATEGORY_CHOICES,
        default='OTHER',
        help_text="Category of this skill"
    )
    proficiency = models.PositiveIntegerField(
        default=50,
        help_text="Proficiency level (0-100)"
    )
    order = models.PositiveIntegerField(
        default=0,
        help_text="Display order"
    )
    is_featured = models.BooleanField(
        default=False,
        help_text="Show in featured skills section"
    )
    auto_generated = models.BooleanField(
        default=False,
        help_text="Was this auto-generated from projects"
    )
    
    class Meta:
        ordering = ['-is_featured', 'order', 'name']
        verbose_name = "Skill"
        verbose_name_plural = "Skills"
    
    def __str__(self):
        return self.name
